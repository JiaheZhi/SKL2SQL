      (
        SELECT
          ("Temperature(F)" -0.0) / (18.948923924075153) AS "Temperature(F)",
          ("Wind_Chill(F)" -0.0) / (20.33996354611891) AS "Wind_Chill(F)",
          ("Humidity(%)" -0.0) / (22.940857900228604) AS "Humidity(%)",
